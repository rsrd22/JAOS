package HISTORIA_CLINICA;

import java.awt.Image;

/**
 *
 * @author mary
 */
public class botonesDeDesplazamiento {
    public int x;
    public int y;
    public int ancho;
    public int alto;
    public Image imagen;

    public botonesDeDesplazamiento(int x, int y, int ancho, int alto, Image imagen) {
        this.x = x;
        this.y = y;
        this.ancho = ancho;
        this.alto = alto;
        this.imagen = imagen;
    }
    
    
}
