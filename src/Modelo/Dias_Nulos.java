package Modelo;

/**
 *
 * @author richard
 */
public class Dias_Nulos {
    public String fecha;
    public String hora;

    public Dias_Nulos(String fecha, String hora) {
        this.fecha = fecha;
        this.hora = hora;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }
    
}
