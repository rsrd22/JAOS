    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed


        if(lblIdConcepto.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Por favor seleccione un concepto para realizar la operación.");
            return;
        }
        if(txtCantidad.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Por favor ingrese la cantidad.");
            return;
        }
        if(txtValorU.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Por favor ingrese el valor a cobrar.");
            return;
        }
        
        if(fila>=0){
            
            String[] datos = new String[]{lblIdConcepto.getText(), txtConcepto.getText(),
                lblDescripcionConcepto.getText(), txtCantidad.getText(), txtValorU.getText(),
                txtValorT.getText(), ""+lblTipoConcepto.getText(), "", lblId_Tratamiento.getText()};
            vf.datos = datos; 
            vf.ActualizarTablaConceptos(fila);
        }else{
            String[] datos = new String[]{lblIdConcepto.getText(), txtConcepto.getText(),
                lblDescripcionConcepto.getText(), txtCantidad.getText(), txtValorU.getText(),
                txtValorT.getText(), ""+lblTipoConcepto.getText(), "", lblId_Tratamiento.getText()};
            vf.datos = datos;
            vf.AgregarTablaConceptos();
        }
        
        this.dispose();

    }//GEN-LAST:event_btnGuardarActionPerformed

