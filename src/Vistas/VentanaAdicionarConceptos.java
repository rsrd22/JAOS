    private void txtCantidadKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCantidadKeyReleased
        System.out.println("txtCantidad.getText()-->"+txtCantidad.getText());
        System.out.println("txtCantidad.getText().length()-->"+txtCantidad.getText().length());
        Expresiones.procesarSoloNumeros(txtCantidad);
        if(txtCantidad.getText().length()>=3){
            txtCantidad.setText(txtCantidad.getText().substring(0, 2));
        }
        totalPago();
    }//GEN-LAST:event_txtCantidadKeyReleased

    private void txtValorUKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtValorUKeyReleased
        String valoru = txtValorU.getText();     
        if(!valoru.equals("")){
            String valorsin = valoru.indexOf(".")>-1?valoru.replace(".", ""):valoru;
            System.out.println("valorsin--->"+valorsin);
            String dato = Expresiones.procesarSoloNum(valorsin);
            if(dato.length()>=9){
                dato  = dato.substring(0, 9);
            }  
            dato = Utilidades.MascaraMoneda(dato);
            txtValorU.setText(dato);
            totalPago();
            EstadoFormulario(1);
        }else{
            totalPago();
            EstadoFormulario(0);
        }
    }//GEN-LAST:event_txtValorUKeyReleased

