    private void LimpiarVentanaAnularFactura() {
        EstadoBotones(0);
        txtNroFactura.setText("");
        txtNombre.setText("");
        txtFecha.setText("");
        txtTipyDoc.setText("");
        txtValor.setText("");
        txtDescripcion.setText("");

    }

    public void EstadoBotones(int opc) {
        if (opc == 0) {//Descarta
            btnAnular.setEnabled(false);
            btnDescartar.setEnabled(false);
            btnbuscarFacturas.setEnabled(true);
            txtDescripcion.setEnabled(false);
        } else {
            btnAnular.setEnabled(true);
            btnDescartar.setEnabled(true);
            btnbuscarFacturas.setEnabled(false);
        }
        txtDescripcion.setEnabled(opc != 0);
    }

    private void AnularFactura() {
        String idpaciente = txtTipyDoc.getText();
        String nroFact = txtNroFactura.getText();
        String observacion = Utilidades.CodificarElemento(txtDescripcion.getText());

        cntlcfact = new ControlFacturas();
        String d = cntlcfact.AnularFactura(idpaciente, nroFact, observacion);

        if (!d.equals("")) {
            JOptionPane.showMessageDialog(null, "La operación se realizo con exito.");
            LimpiarVentanaAnularFactura();
        } else {
            JOptionPane.showMessageDialog(null, "Ocurrio un Error al momento de anular la factura.");
        }

    }

    public void setAanulatFactura(DefaultTableModel dt, int fila) {
        txtTipyDoc.setText((String) dt.getValueAt(fila, 0));
        txtNombre.setText((String) dt.getValueAt(fila, 1));
        txtNroFactura.setText((String) dt.getValueAt(fila, 2));
        txtFecha.setText((String) dt.getValueAt(fila, 3));
        txtValor.setText(((String) dt.getValueAt(fila, 4)).replaceAll(",", "."));
        EstadoBotones(1);
    }
