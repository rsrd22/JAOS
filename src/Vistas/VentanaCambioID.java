    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String id = txtIdentificacion.getText();
        String tid = vp.lista_tpoID.get(cbTidentificacion.getSelectedIndex());
        String idO = vp.txtId.getText();
        String tidO = vp.lista_tpoID.get(vp.cbTid.getSelectedIndex());
        Map<String, String> campos = new HashMap<String, String>();
        campos.put("id", id);
        campos.put("tid", tid);

        campos.put("idO", idO);
        campos.put("tidO", tidO);

        boolean ret = paciente.CambioIdentificacion(con, campos);
        if(ret){
            this.dispose();
            vp.txtId.setText(id);
            vp.tipoId = tid; 
            vp.ConsultarPaciente();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

