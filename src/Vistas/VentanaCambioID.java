    private void LimpiarFormulario() {
        txtIdentificacion.setText("");
        cbTidentificacion.setSelectedIndex(0);
        cbTidentificacion.requestFocus();
        btnGuardar.setEnabled(false);
    }

    private void VerificarIdentificacion() {
        String id = txtIdentificacion.getText();
        String tid = vp.lista_tpoID.get(cbTidentificacion.getSelectedIndex());
        
        ArrayList<String[]> Info = new ArrayList<>();
        Info = paciente.getPaciente(id, tid);
        if(Info.size()>0){//YA SE ENCUENTRA EL TID Y EL ID EN LA BASE DE DATOS
            JOptionPane.showMessageDialog(this, "La identificación ingresada pertenece a la siguiente persona.....");
            LimpiarFormulario();
           
        }else{//REALIZAR EL CAMBIO DE ID
            btnGuardar.setEnabled(true);
            
        }
        System.out.println("info--->"+Info.size());
    }
