    private void passAntKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_passAntKeyPressed
        System.out.println("-evt-  >"+evt.getKeyCode());
        System.out.println("-KeyEvent-  >"+KeyEvent.VK_TAB);
        System.out.println("-KeyEvent-  >"+KeyEvent.VK_T);
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            String clveAnt = passAnt.getText();
            String clved = gsql.getClaveEncryptada(usuario.toUpperCase(), clveAnt);
            System.out.println("clveAnt----"+clveAnt);
            if (!clveAnt.equals("")) {
                enter = 1;
                
                System.out.println("usuario-->"+usuario);
                System.out.println("clve-->"+clve);
                System.out.println("clved---"+clved);  
                
                if (!clved.equals(clve)) {
                    JOptionPane.showMessageDialog(null, "La Contraseña ingresada no corresponde a la contraseña actual");
                    passAnt.setText("");
                    passAnt.requestFocusInWindow();
                } else {
                    passNew.setEnabled(true);
                    passRep.setEnabled(true);
                    btnGuardar.setEnabled(true);
                    passNew.requestFocusInWindow();
                }
            }
        }

    }//GEN-LAST:event_passAntKeyPressed

