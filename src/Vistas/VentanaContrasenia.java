    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String clveAnt = passAnt.getText();
        String clveNew = passNew.getText();
        String clveRep = passRep.getText();
        if(lostR == 0){
            if(clveNew.equals(clveRep)){
                boolean ingr = us.CambiarClave(usuario, clveAnt, clveNew);
                if(ingr){
    //                JOptionPane.showMessageDialog(null, "La operación se realizo exitosamente.");
                    this.dispose();            
                }else{
    //                JOptionPane.showMessageDialog(null, "Ocurrio un error al momento de realizar la operación.");
                }
            }else{
                JOptionPane.showMessageDialog(null, "La Contraseña ingresada no corresponde a la contraseña nueva");
                passRep.setText("");
                passRep.requestFocusInWindow();
            }
        }else{
            lostR = 0;
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

