    private void passRepKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_passRepKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            String clveNew = passNew.getText();        
            String clveRep = passRep.getText();
            enterR = 1;
            if(!clveNew.equals(clveRep)){
                JOptionPane.showMessageDialog(null, "La Contraseña ingresada no corresponde a la contraseña nueva");
                passRep.setText("");
                passRep.requestFocusInWindow();
            }
        }
    }//GEN-LAST:event_passRepKeyPressed

    private void passRepFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_passRepFocusLost
        String clveNew = passNew.getText();
        String clveRep = passRep.getText();
        if(enterR == 0){
            
            if(!clveNew.equals(clveRep)){
                lostR = 1;
                JOptionPane.showMessageDialog(null, "La Contraseña ingresada no corresponde a la contraseña nueva");
                passRep.setText("");
                passRep.requestFocusInWindow();
            }
        }else{
            enterR = 0;
        }
    }//GEN-LAST:event_passRepFocusLost

    private void passNewFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_passNewFocusLost
        String clveNew = passNew.getText();
        String clveRep = passRep.getText();
        if(enterN == 0){
            if(!clveNew.equals(clveRep) && !clveRep.equals("")){
                JOptionPane.showMessageDialog(null, "La Contraseña ingresada no corresponde a la contraseña repetida");
                passNew.setText("");
                passNew.requestFocusInWindow();
            }else{
                passRep.requestFocusInWindow();
            }
        }else{
            enterN = 0;
        }
    }//GEN-LAST:event_passNewFocusLost

