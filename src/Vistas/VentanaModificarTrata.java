    private void txtCostoTotalKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCostoTotalKeyReleased
        String costo = txtCostoTotal.getText();
        String valorsin = costo.indexOf(".")>-1?costo.replace(".", ""):costo;
        System.out.println("valorsin--->"+valorsin);
        String dato = Expresiones.procesarSoloNum(valorsin);
        if(dato.length()>=9){
            dato  = dato.substring(0, 9);
        }
        dato = Utilidades.MascaraMoneda(dato);
        txtCostoTotal.setText(dato);
    }//GEN-LAST:event_txtCostoTotalKeyReleased

