    private boolean validarCorreo(JTextField txt) {
        Pattern pat = Pattern.compile("^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,4})$");
        Matcher mat = pat.matcher(txt.getText());
        boolean ret = false;
        if (mat.matches()) {
            System.out.println("SI");
            ret = true;
        } else {
            txt.setText("");
            System.out.println("NO");
            JOptionPane.showMessageDialog(rootPane, "Escriba un correo electronico valido");
            //txt.requestFocus();
        }
        return ret;
    }
    
    
