    public void ConsultarPersonal() {  
        String id = txtIdentificacion.getText();
        String tid = "";
        
        if(!tipoId.equals("")){  
            EstadoTIdentificacion();  
            tid = tipoId; 
            tipoId = "";  
        }else{  
            tid = lista_tpoID.get(cbTidentificacion.getSelectedIndex());
        }        
        
        boolean pac = (paciente.getPaciente(id, tid).size() > 0);  
        if(pac){            
            JOptionPane.showMessageDialog(rootPane, "La identificación digitada ya se encuentra registrada para un paciente. \nPor favor verifique la información e intentelo nuevamente.");
            txtIdentificacion.setText("");
            //txtId.requestFocusInWindow();
            return;
        }
        if(id.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Por favor digite una identificación");
            //txtIdentificacion.requestFocusInWindow();
            return;
        }
        
        if(tid.equals("Seleccionar")) {
            //cbTidentificacion.requestFocusInWindow();
            JOptionPane.showMessageDialog(rootPane, "Por favor escoja un tipo de identificación");            
            return;
        }
        ArrayList<String[]> Info = new ArrayList<>();
        Info = personal.getPersonal(id, tid);

        //RECORRO LISTA CON INFO
        
        if(Info != null){
            if(Info.size() > 0){
                String[] datos = Info.get(0);
                txtNombre1.setText(datos[0]);
                txtApellido1.setText(datos[1]);
                txtDireccion.setText(datos[2]);
                cbSexo.setSelectedItem(datos[3]);
                jdFNac.setCalendar(formatString(datos[4]));
                txtCorreo.setText(datos[5]);
                String[] tels = (datos[6].equals("")?null:datos[6].split("<>"));
                if(tels != null){
                    System.out.println("tels--->"+tels.length);
                    for(int i = 0; i < tels.length; i++){
                        modlistTelefonos.addElement(tels[i]);
                    }  
                    lstTelefonos.setModel(modlistTelefonos);
                }

                txtTitulo.setText(datos[7]);
                cbCargo.setSelectedItem(datos[8]);
                txtSalario.setText(datos[9]);
                jdFVinculacion.setCalendar(formatString(datos[10]));
                EstadoFormulario(3);
            }else{
                EstadoFormulario(1);
            }            
        } else {
            EstadoFormulario(1);
        }
    }

