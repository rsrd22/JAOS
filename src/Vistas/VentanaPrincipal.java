    public String getHora(String hora) {
        try {
            String[] dat = hora.split(":");
            int h = Integer.parseInt(dat[0]);
            int b = 0;

            if (h >= 12) {
                b = 1;
                if (h > 12) {
                    h = h - 12;
                }
            }

            return h + ":" + dat[1] + " " + (b == 0 ? "a." : "p.") + " m.";

        } catch (Exception e) {
            System.out.println("ERROR--getHora--" + e.toString());
            return "";
        }
    }

